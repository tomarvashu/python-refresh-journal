# Day 3 - Loops Practice

branches = ["Delhi", "Chennai", "Bangalore", "Ludhiana"]

print("Using for loop")
for branch in branches:
    print("Branch:", branch)

print("\nUsing range")
for number in range(1, 6):
    print("Review number:", number)

print("\nUsing while loop")
count = 1
while count <= 3:
    print("Follow-up round:", count)
    count += 1
